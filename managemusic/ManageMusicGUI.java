// Method to search for a song in all playlists
private String searchSong(String songName) {
    // Initialize a StringBuilder to store the search result
    StringBuilder result = new StringBuilder();
    
    // Initialize a boolean flag to track if the song is found
    boolean found = false;
    
    // Search for the song in each playlist and update the result accordingly
    if (likedPlaylist.contains(songName)) {
        result.append(songName).append(" found in Liked Playlist\n");
        found = true;
    }
    if (genre1Playlist.contains(songName)) {
        result.append(songName).append(" found in Pop Playlist\n");
        found = true;
    }
    if (genre2Playlist.contains(songName)) {
        result.append(songName).append(" found in Rap Playlist\n");
        found = true;
    }
    if (playlistManager.getRepeatPlaylist().contains(songName)) {
        result.append(songName).append(" found in Repeat Playlist\n");
        found = true;
    }
    
    // If the song is not found in any playlist, indicate so in the result
    if (!found) {
        result.append("Song not found in any playlist!\n");
    }
    
    // Return the search result as a string
    return result.toString();

    }//GEN-LAST:event_SearchBtnActionPerformed

