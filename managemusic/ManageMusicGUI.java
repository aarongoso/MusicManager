    private void MoveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MoveBtnActionPerformed
        // TODO add your handling code here:
        // Get the song name from the text field
    String song = SongTf.getText().trim();

    // Check if the song name is empty
    if (song.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a song name.", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Exit the method if song name is empty
    }

    // Check if the song exists in either genre1Playlist or genre2Playlist
    boolean songExistsInGenre1 = genre1Playlist.contains(song);
    boolean songExistsInGenre2 = genre2Playlist.contains(song);

    // If the song doesn't exist in either playlist, show an error message
    if (!songExistsInGenre1 && !songExistsInGenre2) {
        JOptionPane.showMessageDialog(this, "The song '" + song + "' does not exist in any playlist.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Prompt the user to select the destination playlist
    String[] playlists = {"Pop Playlist", "Rap Playlist"};
    String selectedPlaylist = (String) JOptionPane.showInputDialog(this, "Select playlist to move the song to:",
            "Select Playlist", JOptionPane.QUESTION_MESSAGE, null, playlists, playlists[0]);

    // Check if user canceled the operation
    if (selectedPlaylist == null) {
        JOptionPane.showMessageDialog(this, "Operation canceled by the user.", "Canceled", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Remove the song from the old playlist and add it to the new playlist
    if (selectedPlaylist.equals("Pop Playlist")) {
        if (songExistsInGenre2) {
            // Remove the song from genre2Playlist
            genre2Playlist.removeSong(song);
        }
        // Add the song to genre1Playlist
        genre1Playlist.addSong(song);
    } else if (selectedPlaylist.equals("Rap Playlist")) {
        if (songExistsInGenre1) {
            // Remove the song from genre1Playlist
            genre1Playlist.removeSong(song);
        }
        // Add the song to genre2Playlist
        genre2Playlist.addSong(song);
    }

    // Update the text area to reflect the changes
    updateTextArea();
    }//GEN-LAST:event_MoveBtnActionPerformed

    private void DeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBtnActionPerformed
        // TODO add your handling code here:
         // Get the song name
    String song = SongTf.getText().trim();

    // Check if the song name is empty
    if (song.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a song name.", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Exit the method if song name is empty
    }

    // Prompt the user to select a playlist
    String[] playlists = {"Liked Playlist", "Pop Playlist", "Rap Playlist", "Repeat Playlist"};
    String selectedPlaylist = (String) JOptionPane.showInputDialog(this, "Select playlist to delete the song from:",
            "Select Playlist", JOptionPane.QUESTION_MESSAGE, null, playlists, playlists[0]);

    // Check if user canceled the operation
    if (selectedPlaylist == null) {
        JOptionPane.showMessageDialog(this, "Operation canceled by the user.", "Canceled", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Delete the song from the selected playlist
    switch (selectedPlaylist) {
        case "Liked Playlist":
            if (!likedPlaylist.contains(song)) {
                JOptionPane.showMessageDialog(this, "The song '" + song + "' does not exist in the Liked Playlist.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            likedPlaylist.removeSong(song);
            break;
        case "Pop Playlist":
            if (!genre1Playlist.contains(song)) {
                JOptionPane.showMessageDialog(this, "The song '" + song + "' does not exist in the Pop Playlist.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            genre1Playlist.removeSong(song);
            break;
        case "Rap Playlist":
            if (!genre2Playlist.contains(song)) {
                JOptionPane.showMessageDialog(this, "The song '" + song + "' does not exist in the Rap Playlist.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            genre2Playlist.removeSong(song);
            break;
        case "Repeat Playlist":
            if (!playlistManager.getRepeatPlaylist().contains(song)) {
                JOptionPane.showMessageDialog(this, "The song '" + song + "' does not exist in the Repeat Playlist.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            playlistManager.getRepeatPlaylist().removeSong(song);
            break;
        default:
            JOptionPane.showMessageDialog(this, "Invalid playlist selected.", "Error", JOptionPane.ERROR_MESSAGE);
    }

    // Update the text area
    updateTextArea();


    }//GEN-LAST:event_DeleteBtnActionPerformed

