    sb.append("Number of songs in Rap Playlist: ").append(genre2Count).append("\n");
    sb.append("Number of songs in Repeat Playlist: ").append(repeatCount).append("\n");
    
    // Set the text in the text area to display the counts
    TextArea.setText(sb.toString());
