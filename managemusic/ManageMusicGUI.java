    // Method to get songs from the selected playlist
private String getSongsFromPlaylist(String playlistName) {
    Playlist selectedPlaylist = null;
    switch (playlistName) {
        case "Liked Playlist":
            selectedPlaylist = likedPlaylist;
            break;
        case "Pop Playlist":
            selectedPlaylist = genre1Playlist;
            break;
        case "Rap Playlist":
            selectedPlaylist = genre2Playlist;
            break;
        case "Repeat Playlist":
            selectedPlaylist = playlistManager.getRepeatPlaylist();
            break;
        default:
            // This should not happen, but if it does, return an empty string
            return "";
    }

    List<String> songs = selectedPlaylist.getSongs();
    if (songs.isEmpty()) {
        return "There are no songs in this playlist.";
    } else {
        StringBuilder playlistContent = new StringBuilder();
        for (String song : songs) {
            playlistContent.append(song).append("\n");
        }
        return playlistContent.toString();
    }
}
    
